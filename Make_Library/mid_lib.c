int mod(int a, int b){
        return a%b;
}
int abs2(int a, int b){
        if(a > b){
		return a-b;
	}
	else{
		return b-a;
	}
}
int miny(int a, int b){
	if(a < b){
		return a;
	}
	else{
		return b;
	}
}
int maxy(int a, int b){
        if(a > b){
                return a;
        }
        else{
                return b;                                                             }
}
